# 按位与
print(5 & 7)  # 5: 0101, 7: 0111, 结果: 0101 = 5

# 按位或
print(5 | 7)  # 5: 0101, 7: 0111, 结果: 0111 = 7

# 按位异或
print(5 ^ 7)  # 5: 0101, 7: 0111, 结果: 0010 = 2

# 按位取反
print(~5)  # 5: 0101, 结果: 1010 = -6 (补码表示)

# 左移动运算符
print(5 << 1)  # 5: 0101, 结果: 1010 = 10 (左移一位，相当于乘以2)
print(5 << 2)  # 5: 0101, 结果: 10100 = 20 (左移两位，相当于乘以4)

# 右移动运算符
print(5 >> 1)  # 5: 0101, 结果: 0010 = 2 (右移一位，相当于除以2)
print(5 >> 2)  # 5: 0101, 结果: 0001 = 1 (右移两位，相当于除以4)